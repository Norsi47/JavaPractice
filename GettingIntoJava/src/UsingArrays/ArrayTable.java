package UsingArrays;

public class ArrayTable {
    public static void main(String[] args) {
        //the \t gave a space in between
        System.out.println("Index Value");

        int norsi [] = {32, 18, 54, 2, 12};

        // for loops start with zero because first value is zero
        for(int i = 0; i < norsi.length; i++) {
            System.out.println(i + " \t " + norsi[i]);
    }




    }
}
