public class MathClassMethods {

    public static void main(String[] args) {

        //everything after the . is called a method name

        //abs staands for absolute value, eg -26.6 = 26.7
        System.out.println(Math.abs(-26.7));

        //ceil will wrong up a number eg 7.4 = 8
        System.out.println(Math.ceil(7.5));

        //this will round down the number 7.4 = 7
        System.out.println(Math.floor(7.8));

        // will get maximum of the two numbers eg 8.6 is the biggest in this arguments
        System.out.println(Math.max(8.6, 5.2));

        //power 5 * 5 * % = 125
        System.out.println(Math.pow(5, 3));

        //square root of 9 is three
        System.out.println("Square Root is: "+Math.sqrt(9));

    }


}
