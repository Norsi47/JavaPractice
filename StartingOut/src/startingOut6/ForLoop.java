package startingOut6;

public class ForLoop {
    public static void main(String[] args) {
        //will start at 1 and stop at 10, the ++ means it will show up one at a time
        //counter +=3 will add 3 to the next one
        //takes 3 parameters
        for(int counter=6; counter <=21; counter+=3) {
            System.out.println(counter);

        }


    }
}
